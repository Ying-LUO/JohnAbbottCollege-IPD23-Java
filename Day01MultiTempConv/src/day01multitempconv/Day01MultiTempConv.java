    private void tfOutputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfOutputActionPerformed
        // TODO add your handling code here:
        
        tfOutput.setEditable(false);
        
        try{
            Double inputValue = Double.parseDouble(tfInput.getText());
            Double outputValue;
            String result;
                    
            if(btInputKelvin.isSelected() && btOutputCelsius.isSelected()){
                outputValue = inputValue - 273.15;
                
            }else if(btInputKelvin.isSelected() && btOutputFah.isSelected()){
                outputValue = ( inputValue - 273.15 ) * 9 / 5 + 32;
                
            }else if(btInputFah.isSelected() && btOutputCelsius.isSelected()){
                outputValue = ( inputValue - 32) * 5 / 9;
                
            }else if(btInputFah.isSelected() && btOutputKelvin.isSelected()){
                outputValue = ( inputValue - 32) * 5 / 9 + 273.15;
                
            }else if(btInputCelsius.isSelected() && btOutputKelvin.isSelected()){
                outputValue = inputValue + 273.15;
                
            }else if(btInputCelsius.isSelected() && btOutputFah.isSelected()){
                outputValue = inputValue * 9 / 5 + 32;
                
            }else{
                JOptionPane.showMessageDialog(null, "Please choose different Input and Output temperatures");
                outputValue = inputValue;
                
            }
            
            result = String.format("%.2f Â°F", outputValue);
            tfOutput.setText(result);
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Exception", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tfOutputActionPerformed

